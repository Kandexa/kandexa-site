function setLang(lang){
  const l = (lang === "en") ? "en" : "tr";
  window.__i18n_lang = l;
  localStorage.setItem("kandexa_lang", l);

  const dict = I18N[l];
  document.querySelectorAll("[data-i18n]").forEach(el=>{
    const key = el.getAttribute("data-i18n");
    if(!dict[key]) return;
    if(el.tagName === "INPUT" || el.tagName === "TEXTAREA"){
      el.setAttribute("placeholder", dict[key]);
    } else {
      // preserve line breaks for hero title
      if(key === "hero_title"){
        el.innerHTML = dict[key].replace(/\n/g, "<br>");
      } else {
        el.textContent = dict[key];
      }
    }
  });

  // update lang button text
  const btn = document.getElementById("langBtn");
  if(btn){
    btn.querySelector(".lang-pill").textContent = l.toUpperCase();
  }
}

function toggleLang(){
  const current = localStorage.getItem("kandexa_lang") || "tr";
  setLang(current === "tr" ? "en" : "tr");
}

document.addEventListener("DOMContentLoaded", ()=>{
  const saved = localStorage.getItem("kandexa_lang") || "tr";
  setLang(saved);
});
